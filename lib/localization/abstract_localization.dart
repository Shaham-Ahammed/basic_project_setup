// Authentication Feature Localization
abstract class AuthLocalization {
  String get loginTitle;
  String get signupTitle;
  String get forgotPasswordTitle;
}

// Home Feature Localization
abstract class HomeLocalization {
  String get homeTitle;
  String get welcomeMessage;
}

// Profile Feature Localization
abstract class ProfileLocalization {
  String get profileTitle;
  String get editProfile;
}

// Error Feature Localization
abstract class ErrorLocalization {
  String get errorGeneric;
  String get networkError;
}
